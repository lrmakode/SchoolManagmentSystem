    private void feeBRUPTableKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_feeBRUPTableKeyReleased
        Vector fullVec = dtmFeeBRUP.getDataVector();
        Vector colSum = new Vector(5);
        for (int i = 0; i < 5; i++) {
            colSum.add((float) 0);
        }

        for (int i = 0; i < fullVec.size(); i++) {
            Vector row = (Vector) fullVec.get(i);
            float sum = 0;
            for (int k = 1; k < row.size() - 1; k++) {
                Object o = row.get(k);
                if (o != null) {
                    sum += (float) o;
                    Object tob = colSum.get(k - 1);
                    if (tob != null) {
                        colSum.setElementAt((float) o + (float) tob, k - 1);
                    }
                }
            }
            dtmFeeBRUP.setValueAt(sum, i, 6);
        }
        int i = 0;
        float total = (float) 0;
        for (Object o : colSum) {
            if (o != null) {
                total += (float) o;
                dtmFee.setValueAt((float) o, i, 1);
            }
            i++;
        }
        dtmFee.setValueAt(total, i, 1);
    }//GEN-LAST:event_feeBRUPTableKeyReleased

    private void feeTableKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_feeTableKeyReleased
        Object ob = null;
        float total = 0;
        for (int k = 0; k < 5; k++) {
            ob = null;
            ob = dtmFee.getValueAt(k, 1);
            if (ob != null) {
                float tutFee = ((float) ob);
                total += tutFee;
                tutFee /= dtmFeeBRUP.getRowCount();
                for (int i = 0; i < dtmFeeBRUP.getRowCount(); i++) {
                    dtmFeeBRUP.setValueAt(tutFee, i, k + 1);
                }
            }
        }
        dtmFee.setValueAt(total, 5, 1);
        total /= dtmFeeBRUP.getRowCount();
        for (int i = 0; i < dtmFeeBRUP.getRowCount(); i++) {
            dtmFeeBRUP.setValueAt(total, i, 6);
        }
    }//GEN-LAST:event_feeTableKeyReleased

