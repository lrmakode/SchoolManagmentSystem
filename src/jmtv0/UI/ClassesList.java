    private int saveModifyClass() {
        boolean isEdit = ckBoxEdit.isSelected();
        if (isEdit) {
            if (mClass == null) {
                return -1;
            }
        }
        if (ckBoxEdit.isSelected() || !newButton.isEnabled()) {
            String str = tfClassName.getText();
            if (str.length() != 0) {
                Session ses = HibernateUtil.getSessionFactory().openSession();
                CLASS lClass = null;
                if (isEdit) {
                    lClass = Utility.getClassWithID(mClass.getIdClass(), ses);
                } else {
                    lClass = new CLASS();
                }
                lClass.setNameOfClass(str);
                lClass.setFromYear(start.getTime());
                lClass.setToYear(end.getTime());
                lClass.setDueDay(dayOfMonth.getSelectedIndex());
                lClass.setPenaltyCycle(comBoxPenaltyCycle.getSelectedIndex());
                str = tfPanltyRate.getText();
                if (str.length() > 0) {
                    lClass.setLateFeePercent(Float.parseFloat(str));
                }
                lClass.setLastUpdate(new Date());
                FEEDETAILS lfeeDTLS = null;
                if (isEdit) {
                    lfeeDTLS = Utility.getFeeDTLSWithID(mClass.getFeeDetails().getIdFeeDetails(), ses);
                } else {
                    lfeeDTLS = new FEEDETAILS();
                }
                Vector feeDV = dtmFee.getDataVector();
                Object lTmp = ((Vector) feeDV.get(0)).get(1);
                if (lTmp != null) {
                    lfeeDTLS.setTutionFee((float) lTmp);
                }
                lTmp = ((Vector) feeDV.get(1)).get(1);
                if (lTmp != null) {
                    lfeeDTLS.setLibraryFee((float) lTmp);
                }
                lTmp = ((Vector) feeDV.get(2)).get(1);
                if (lTmp != null) {
                    lfeeDTLS.setStationaryFee((float) lTmp);
                }
                lTmp = ((Vector) feeDV.get(3)).get(1);
                if (lTmp != null) {
                    lfeeDTLS.setDressFee((float) lTmp);
                }
                lTmp = ((Vector) feeDV.get(4)).get(1);
                if (lTmp != null) {
                    lfeeDTLS.setMiscFee((float) lTmp);
                }
                lTmp = ((Vector) feeDV.get(5)).get(1);
                if (lTmp != null) {
                    lfeeDTLS.setTotalFee((float) lTmp);
                }
                ses.beginTransaction();
                try {
                    if (isEdit) {
                        ses.saveOrUpdate(lfeeDTLS);
                        ses.saveOrUpdate(lClass);

                    } else {
                        ses.save(lfeeDTLS);
                        lClass.setFeeDetails(lfeeDTLS);
                        ses.save(lClass);
                    }
                    DefaultTableModel dtmFeeBRUPTable = (DefaultTableModel) feeBRUPTable.getModel();
                    Vector feeBRDV = dtmFeeBRUPTable.getDataVector();
                    if (feeBRDV != null) {

                        Calendar dueDate = Calendar.getInstance();
                        dueDate.setTime(start.getTime());

                        Calendar classDate = Calendar.getInstance();
                        classDate.setTime(dueDate.getTime());
                        dueDate.set(Calendar.DATE, dayOfMonth.getSelectedIndex());

                        for (Object obj : feeBRDV) {
                            Vector lRow = (Vector) obj;
                            if (lRow != null) {
                                FEEBREAKUP lFeeBRUP = null;
                                if (isEdit) {
                                    lFeeBRUP = Utility.getFeeBRUPWithFeeDetailsIDAndMonth(lfeeDTLS.getIdFeeDetails(), classDate.get(Calendar.MONTH), ses);
                                    if (lFeeBRUP == null) {
                                        lFeeBRUP = new FEEBREAKUP();
                                    }
                                } else {
                                    lFeeBRUP = new FEEBREAKUP();
                                }
                                lFeeBRUP.setMonth(classDate.getTime());
                                if (null != lRow.get(1)) {
                                    lFeeBRUP.setTutionFee((float) lRow.get(1));
                                }
                                if (null != lRow.get(2)) {
                                    lFeeBRUP.setLibraryFee((float) lRow.get(2));
                                }
                                if (null != lRow.get(3)) {
                                    lFeeBRUP.setStationaryFee((float) lRow.get(3));
                                }
                                if (null != lRow.get(4)) {
                                    lFeeBRUP.setDressFee((float) lRow.get(4));
                                }
                                if (null != lRow.get(5)) {
                                    lFeeBRUP.setMiscFee((float) lRow.get(5));
                                }
                                if (null != lRow.get(6)) {
                                    lFeeBRUP.setTotalFee((float) lRow.get(6));
                                }
                                lFeeBRUP.setIdFeeDTLS(lfeeDTLS);
                                lFeeBRUP.setDueDate(dueDate.getTime());

                                ses.saveOrUpdate(lFeeBRUP);
                            }
                            dueDate.add(Calendar.MONTH, 1);
                            classDate.add(Calendar.MONTH, 1);

                        }
                    }
                } catch (Exception e) {
                    ses.getTransaction().rollback();
                    e.printStackTrace();
                    return -2;
                }
                ses.getTransaction().commit();
                ses.close();
                return 1;
            }
        } else {
            return -1;
        }
        return -1;
    }

