    private void classTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_classTableMouseClicked
        if (!checkOfSave()) {
            Vector colsFeeBRUP = new Vector();
            colsFeeBRUP.addElement("Month");
            colsFeeBRUP.add("Tution Fee");
            colsFeeBRUP.add("Library Fee");
            colsFeeBRUP.add("Stationary Fee");
            colsFeeBRUP.add("Dress Fee");
            colsFeeBRUP.add("Misc. Fee");
            colsFeeBRUP.add("Total Fee");
            ckBoxEdit.setSelected(false);
            int ids = classTable.getSelectedRow();
            Object o = dtmClassTable.getValueAt(ids, 0);
            if (dtmClassTable.getValueAt(ids, 0) != null) {
                Session ses = HibernateUtil.getSessionFactory().openSession();
                try {
                    mClass = Utility.getClassWithID((int) dtmClassTable.getValueAt(ids, 0), ses);
                    updateClassForm();
                    Vector rowsFeeBRUP = new Vector();
                    List l = Utility.getFeeBrupListWithFeeDetailsID(mClass.getFeeDetails().getIdFeeDetails(), ses);
                    for (Object fee : l) {
                        Vector row = new Vector();
                        FEEBREAKUP lFeeBRUP = (FEEBREAKUP) fee;
                        Calendar c = Calendar.getInstance();
                        c.setTime(lFeeBRUP.getMonth());
                        String month = Utility.getMonthStrFromNum(c.get(Calendar.MONTH)) + "~" + c.get(Calendar.YEAR);
                        row.add(month);
                        if (lFeeBRUP != null) {
                            row.add(lFeeBRUP.getTutionFee());
                            row.add(lFeeBRUP.getLibraryFee());
                            row.add(lFeeBRUP.getStationaryFee());
                            row.add(lFeeBRUP.getDressFee());
                            row.add(lFeeBRUP.getMiscFee());
                            row.add(lFeeBRUP.getTotalFee());
                        }
                        rowsFeeBRUP.add(row);
                    }
                    DefaultTableModel dtmFeeBRUP = (DefaultTableModel) feeBRUPTable.getModel();
                    dtmFeeBRUP.setDataVector(rowsFeeBRUP, colsFeeBRUP);
                    newButton.setEnabled(true);
                } catch (Exception e) {
                } finally {
                    ses.close();
                }
                UpdateStudentList();
            }
        }
    }//GEN-LAST:event_classTableMouseClicked

